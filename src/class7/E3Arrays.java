package class7;

public class E3Arrays {
    public static void main(String[] args) {
        String[] fruits = {"apple", "orange", "banana", "kiwi", "pineapple"};
        System.out.println(fruits.length);//gives us the number of items in array
        for (int i = 0; i < fruits.length ; i++) {
            System.out.println(fruits[i]);
        }

    }


}



