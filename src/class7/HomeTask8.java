package class7;

public class HomeTask8 {
    /*
Create an array to store char values and then print those in reverse order
     */
    public static void main(String[] args) {
        char[] letters = {'A', 'B', 'C', 'D' };
        for (int i = 3; i >= 0; i--) {
            System.out.print(letters[i] + " ");

        }
    }
}
