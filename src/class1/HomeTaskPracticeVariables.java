package class1;

public class HomeTaskPracticeVariables { //Pascal Case is used for classes
    public static void main(String[] args) {

        //primitive
        //Camel case is used for variables
        //whole numbers:
        byte age =20;    //very small whole box
        short measure=33;//small whole number box
        int height=174;  // most widely used whole number box
        long housePrice=41979164138L; //large whole number box

        //decimal numbers:
        float size= 0.65762F; //medium box for decimal numbers
        double weight =60.25687657625393659; //large box for decimal numbers

        char dollarSign='$'; // single character box
        boolean wantToLearnJavaMore= true; //true or false box

        //non-primitive dta types:

        String fullName="Marta Ostash";


        System.out.println(age);
        System.out.println(height);
        System.out.println(weight);
        System.out.println(housePrice);
        System.out.println(size);
        System.out.println(fullName);
        System.out.println(wantToLearnJavaMore);
        System.out.println(dollarSign);
        System.out.println(measure);

    }
}
