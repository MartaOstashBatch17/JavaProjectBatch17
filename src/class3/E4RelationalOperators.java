package class3;

public class E4RelationalOperators {
    public static void main(String[] args) {

        System.out.println(15>10);
        System.out.println(15<10);


//performed a relational operation stored in result variable
        boolean result=100>205;
        System.out.println(result);

        //you cannot use relational operators with String


        //Ascii table converts letters to numbers that's why we can compare letters and we have to refer to Ascii table
        System.out.println('A'>'B'); //a=65, b=66
        System.out.println('C'>'B'); //c=67, b=66

    }
}
