package class2;

public class E8ArithmeticOperations {
    public static void main(String[] args) {

        int noOfSlices=5;
        int noOfFriends=2;
        System.out.println(noOfSlices/noOfFriends);
        System.out.println(noOfSlices%noOfFriends);

        //10/3 3.3333 if you want your answer in decimal use double
        double num1=10;
        double num2=4;
        System.out.println(num1/num2);
        System.out.println(num1%num2);

    }
}
